

def slide_down( r , final_sc , pos_x):
    r.geometry(f'{final_sc}x50+{pos_x}+0')  # Cambia la altura a 50
# Función para reducir la altura cuando se quite el hover


def slide_up( root , final_sc , pos_x):
    root.geometry(f'{final_sc}x5+{pos_x}+0')  # Cambia la altura a 5
# Función para traer la ventana al frente

